def get_v3_from_json(verse_ref):
    b, c, v = get_book_chapter_verse(verse_ref)
    json_path = f'{v3_json_folder}/{b}/Chapter {c}/{b} {c} {v} bht.json'
    score = json.load(open(json_path))["bestBHT"]["qualityScore"]
    return score

